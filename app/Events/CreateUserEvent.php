<?php

namespace App\Events;

use App\Data\Entities\User;

class CreateUserEvent extends Event
{
    /**
     * @var User
     */
    public $user;

    /**
     * Create a new event instance.
     *
     * @return void
     */
    public function __construct(User $user)
    {
        $this->user = $user;
    }
}
